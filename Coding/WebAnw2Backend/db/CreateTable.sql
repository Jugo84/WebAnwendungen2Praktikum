	Beschreibung TEXT NOT NULL,
	Genre TEXT NOT NULL,
	Trailer TEXT NOT NULL,
	FSK INTEGER NOT NULL DEFAULT 12,
	Dauer INTEGER NOT NULL DEFAULT 90,
	Coverpfad TEXT DEFAULT NULL,
	Videopfad TEXT DEFAULT NULL
);


